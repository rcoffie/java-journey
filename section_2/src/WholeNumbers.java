public class WholeNumbers {
    public static void main(String[] args){

//        int type
        int numberOfFans = 5000;
        System.out.println("There is " + numberOfFans + " fans at the Quidditch games");

//        long type
//        Note kinly add L at the end of every number to let Java knows is a large number
        long globalPopulation = 7000000000L;
        System.out.println("There are " + globalPopulation + " people on earth");

    }
}
